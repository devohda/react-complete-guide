import {Quote} from '../components/quotes/types';

export const sortQuotes = (quotes: Quote[], ascending = false) => {
  return quotes.sort((quoteA, quoteB) => {
    if (ascending) {
      return quoteA.id > quoteB.id ? 1 : -1;
    } else {
      return quoteA.id < quoteB.id ? 1 : -1;
    }
  });
};
